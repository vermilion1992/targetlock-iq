describe("geomag — WMM2025", () => {
  it("matches all 100 official NOAA WMM2025 test values", () => {
    for (const [year, altKm, lat, lon, decl, incl, h, f] of NOAA_TEST_VALUES) {
      const result = computeMagneticDeclination({
        latitudeDeg: lat,
        longitudeDeg: lon,
        elevationKm: altKm,
        date: year,
      });
      const label = `(${year}, ${lat}, ${lon}, ${altKm} km)`;
      // Published declination/inclination are rounded to 0.01 deg.
      expect(
        Math.abs(result.declinationDeg - decl),
        `declination ${label}`
      ).toBeLessThan(0.0051);
      expect(
        Math.abs(result.inclinationDeg - incl),
        `inclination ${label}`
      ).toBeLessThan(0.0051);
      // Intensities are published to micro-nT precision.
      expect(
        Math.abs(result.horizontalIntensityNt - h),
        `H ${label}`
      ).toBeLessThan(0.01);
      expect(Math.abs(result.totalIntensityNt - f), `F ${label}`).toBeLessThan(0.01);
    }
  });

  it("reports model metadata", () => {
    const result = computeMagneticDeclination({
      latitudeDeg: -30,
      longitudeDeg: 135,
      date: 2026.0,
    });
    expect(result.modelEpoch).toBe(WMM_MODEL_EPOCH);
    expect(result.modelName).toBe(WMM_MODEL_NAME);
    expect(result.decimalYear).toBe(2026.0);
    expect(result.warnings).toEqual([]);
  });

  it("accepts a JS Date and converts it to a decimal year", () => {
    const midYear = new Date(Date.UTC(2026, 6, 2)); // ~2026.5
    expect(decimalYearFromDate(midYear)).toBeCloseTo(2026.5, 2);
    const result = computeMagneticDeclination({
      latitudeDeg: -33,
      longitudeDeg: 109,
      elevationKm: 0,
      date: midYear,
    });
    expect(result.decimalYear).toBeCloseTo(2026.5, 2);
    expect(Number.isFinite(result.declinationDeg)).toBe(true);
  });

  it("warns when the date is outside the 2025-2030 validity window", () => {
    const before = computeMagneticDeclination({
      latitudeDeg: -30,
      longitudeDeg: 135,
      date: 2024.5,
    });
    expect(before.warnings.some((w) => w.includes("validity window"))).toBe(true);
    const after = computeMagneticDeclination({
      latitudeDeg: -30,
      longitudeDeg: 135,
      date: 2031.0,
    });
    expect(after.warnings.some((w) => w.includes("validity window"))).toBe(true);
  });

  it("warns inside a magnetic blackout zone (weak horizontal field)", () => {
    // NOAA test point with H ~= 907 nT (< 2000 nT blackout threshold).
    const result = computeMagneticDeclination({
      latitudeDeg: 87,
      longitudeDeg: -154,
      elevationKm: 50,
      date: 2029.0,
    });
    expect(result.warnings.some((w) => w.includes("blackout"))).toBe(true);
  });

  it("rejects non-finite or out-of-range coordinates", () => {
    expect(() =>
      computeMagneticDeclination({ latitudeDeg: 91, longitudeDeg: 0, date: 2026 })
    ).toThrow();
    expect(() =>
      computeMagneticDeclination({ latitudeDeg: NaN, longitudeDeg: 0, date: 2026 })
    ).toThrow();
  });
});
